numero = int(input('Entre com um número de 1 a 7: '))

if numero == 1:
    print('1 - Domingo')
elif numero == 2:
    print('2 - Segunda')
elif numero == 3:
    print('3 - Terça')
elif numero == 4:
    print('4 - Quarta')
elif numero == 5:
    print('5 - Quinta')
elif numero == 6:
    print('6 - Sexta')
elif numero == 7:
    print('7 - Sábado')
else:
    print('Valor inválido')